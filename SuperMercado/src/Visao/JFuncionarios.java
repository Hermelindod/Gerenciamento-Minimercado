    private void JBGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBGuardarActionPerformed
       if (cmbcodtipo.getSelectedIndex() == 0 )
       {
         JOptionPane.showMessageDialog(this,"Selecione a Função!");  
       }
       else{
        try{
            if(validarCampos()){
                if(preencherObjecto()){
                    // Salvar no Banco
                    FuncionarioDados DAO = new FuncionarioDados();
                    if(DAO.incluirFuncionario(obj)){
                        JOptionPane.showMessageDialog(this,"Registo Salvo com sucesso!");
                        limparCampos();
                        JBGuardar.setEnabled(false);
                    }else{
                        JOptionPane.showMessageDialog(this,"Nao foi possivel salvar no banco!");
                    }
                }
            }
        }catch(Exception erro){
            JOptionPane.showMessageDialog(this,"Erro: "
                + erro.getMessage());
        }
        try {
            tabelaUtilizador(jTable1);
        } catch (SQLException ex) {
            Logger.getLogger(JFornecedores.class.getName()).log(Level.SEVERE, null, ex);
        }
       }
    }//GEN-LAST:event_JBGuardarActionPerformed

    private void JBRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBRemoverActionPerformed
        int escolha = JOptionPane.showOptionDialog(null, " DESEJA APAGAR OS DADOS ?", "ESCOLHER OPÇÃO", JOptionPane.DEFAULT_OPTION, JOptionPane.QUESTION_MESSAGE, null, opcao, opcao[0]);

        if (escolha == 0) {
            if(validarCampos()){
                obj = new Funcionario();
                obj.setCodfunc(parseInt(txtcodigo.getText()));
                funcionario.Remover(obj);
                try {
                    tabelaUtilizador(jTable1);
                } catch (SQLException ex) {
                    Logger.getLogger(JFuncionarios.class.getName()).log(Level.SEVERE, null, ex);
                }
                limparCampos();
                JBGuardar.setEnabled(false);
                JBActuaizar.setEnabled(false);
                JBRemover.setEnabled(false);
            }
        }
    }//GEN-LAST:event_JBRemoverActionPerformed

    private void JBActuaizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBActuaizarActionPerformed
        if (cmbcodtipo.getSelectedIndex() == 0 )
       {
         JOptionPane.showMessageDialog(this,"Selecione a Função!");  
       }
        else{
        if(validarCampos()){
            obj = new Funcionario();
            obj.setCodfunc(parseInt(txtcodigo.getText()));
            obj.setNomefunc(txtnome.getText());
            obj.setBifunc(txtbi.getText());
            obj.setEmailfunc(txtemail.getText());
            obj.setTelefonefunc(parseInt(txttelefone.getText()));
            obj.setSenhafunc(txtsenha.getText());
            obj.setCodtipo(cmbcodtipo.getSelectedIndex());
            funcionario.Actualizar(obj);
            try {
                tabelaUtilizador(jTable1);
            } catch (SQLException ex) {
                Logger.getLogger(JFornecedores.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        }
    }//GEN-LAST:event_JBActuaizarActionPerformed

