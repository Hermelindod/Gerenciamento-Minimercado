     private boolean validarCampos(){
          if(txtnome.getText().equals("")){
            JOptionPane.showMessageDialog(this,"Digite o nome!");
            txtnome.requestFocus();
            return false;
            }
          if(txtsenha.getText().equals("")){
            JOptionPane.showMessageDialog(this,"Digite a senha!");
            txtsenha.requestFocus();
            return false;
            }
        return true;
    }
    private boolean preencherObjecto(){
        obj = new Funcionario();
        obj.setNomefunc(txtnome.getText());
        obj.setBifunc(txtbi.getText());
        obj.setEmailfunc(txtemail.getText());
        obj.setTelefonefunc(parseInt(txttelefone.getText()));
        obj.setSenhafunc(txtsenha.getText());
        obj.setCodtipo(cmbcodtipo.getSelectedIndex());
        
        return true;
    }
    private void limparCampos(){
        txtcodigo.setText("");
        txtnome.setText("");
        txtbi.setText("");
        txtemail.setText("");
        txtsenha.setText("");
        txttelefone.setText("");
        cmbcodtipo.getSelectedIndex();
    }

