    public void tabelaUtilizador(JTable tabela) throws SQLException{
        ArrayList dados=new ArrayList();
         String[] colunas=new String[]{"codproduto","descricao","precounit","stock","data","codfornecedor"};
        List<Produto> lista = produto.mostrar();
        
        for(Produto c:lista){
            dados.add(new Object[]{c.getCodproduto(),c.getDescricaoproduto(),c.getPrecounit(),c.getStock(),c.getData(),c.getCodfornecedor()});
        }
       
        Utilizar modelo = new Utilizar(dados, colunas);
        
        tabela.setModel(modelo);
        tabela.getColumnModel().getColumn(0).setPreferredWidth(60);
        tabela.getTableHeader().setReorderingAllowed(false); 
        tabela.getColumnModel().getColumn(1).setPreferredWidth(150);
        tabela.getTableHeader().setReorderingAllowed(false); 
        tabela.getColumnModel().getColumn(2).setPreferredWidth(100);
        tabela.getTableHeader().setReorderingAllowed(false); 
        tabela.getColumnModel().getColumn(3).setPreferredWidth(100);
        tabela.getTableHeader().setReorderingAllowed(false);
        tabela.getColumnModel().getColumn(4).setPreferredWidth(100);
        tabela.getTableHeader().setReorderingAllowed(false);
        tabela.getColumnModel().getColumn(5).setPreferredWidth(100);
        tabela.getTableHeader().setReorderingAllowed(false);
        tabela.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);

    }
