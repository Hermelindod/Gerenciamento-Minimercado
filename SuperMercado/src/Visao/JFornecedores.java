    private void JBRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBRemoverActionPerformed
        int escolha = JOptionPane.showOptionDialog(null, " DESEJA APAGAR OS DADOS ?", "ESCOLHER OPÇÃO", JOptionPane.DEFAULT_OPTION, JOptionPane.QUESTION_MESSAGE, null, opcao, opcao[0]);

        if (escolha == 0) {
            if(validarCampos()){
                obj = new Fornecedor();
                obj.setCodfornecedor(parseInt(txtcodigo.getText()));
                fornecedor.Remover(obj);
                try {
                    tabelaUtilizador(jTable1);
                } catch (SQLException ex) {
                    Logger.getLogger(JFornecedores.class.getName()).log(Level.SEVERE, null, ex);
                }
                limparCampos();
                JBGuardar.setEnabled(false);
                JBActuaizar.setEnabled(false);
                JBRemover.setEnabled(false);
            }
        }
    }//GEN-LAST:event_JBRemoverActionPerformed

