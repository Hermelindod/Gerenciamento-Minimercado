     private boolean validarCampos(){
          if(txtnome.getText().equals("")){
            JOptionPane.showMessageDialog(this,"Digite o nome!");
            txtnome.requestFocus();
            return false;
            }
          if(txtcidade.getText().equals("")){
            JOptionPane.showMessageDialog(this,"Digite o nome!");
            txtcidade.requestFocus();
            return false;
            }
        return true;
    }
    private boolean preencherObjecto(){
        obj = new Fornecedor();
        obj.setNomeforn(txtnome.getText());
        obj.setEmailforn(txtemail.getText());
        obj.setCidadeforn(txtcidade.getText());
        obj.setTelefoneforn(parseInt(txttelefone.getText()));
        return true;
    }
    private void limparCampos(){
        txtcodigo.setText("");
        txtnome.setText("");
        txtemail.setText("");
        txtcidade.setText("");
        txttelefone.setText("");
    }

