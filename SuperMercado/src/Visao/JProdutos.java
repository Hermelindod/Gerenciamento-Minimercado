     private boolean validarCampos(){
          if(txtnome.getText().equals("")){
            JOptionPane.showMessageDialog(this,"Digite o nome!");
            txtnome.requestFocus();
            return false;
            }
          if(txtprecounit.getText().equals("")){
            JOptionPane.showMessageDialog(this,"Digite o nome!");
            txtprecounit.requestFocus();
            return false;
            }
        return true;
    }
    private boolean preencherObjecto(){
        obj = new Produto();
        obj.setDescricaoproduto(txtnome.getText());
        obj.setStock(parseInt(txtstock.getText()));
        obj.setData(txtdata.getText());
        obj.setPrecounit(parseInt(txtprecounit.getText()));
        obj.setCodfornecedor(parseInt(txtcodfornecedor.getText()));
        return true;
    }
